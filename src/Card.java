public class Card {
    private int resource;
    private int value;
    private int shield;

    public int getResource(){
    return resource;
    }
    public void setResource(int x){
        resource=x;
    }
    public int getValue() {
        return value;
    }
    public void setValue(int x){
        value=x;
    }
    public int getShield(){
        return shield;
    }
    public void setShield(int x){
        shield=x;
    }

}
